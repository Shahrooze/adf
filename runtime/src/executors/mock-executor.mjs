// Deterministic executor used for tests, dry runs, and `adf doctor`. It
// never calls out to any LLM: for each artifact filename the current
// workflow stage produces, it resolves the matching template under
// templates/ (falling back to a minimal generic skeleton) and returns that
// as the produced content, with the STATUS line rewritten to the stage's
// gate status when one is provided. This is enough to exercise the entire
// Workflow Engine — sequential, parallel, gated — end to end without any
// external dependency.
import fs from "node:fs";
import path from "node:path";
import { AgentExecutor } from "./agent-executor.mjs";
import { TEMPLATES_DIR } from "../config/paths.mjs";

const IMPLEMENTATION_REPORT_TEMPLATE = "implementation-report.md";

function resolveTemplate(filename) {
  const direct = path.join(TEMPLATES_DIR, filename);
  if (fs.existsSync(direct)) return fs.readFileSync(direct, "utf8");
  if (filename.includes("implementation-report")) {
    const shared = path.join(TEMPLATES_DIR, IMPLEMENTATION_REPORT_TEMPLATE);
    if (fs.existsSync(shared)) return fs.readFileSync(shared, "utf8");
  }
  return null;
}

function genericSkeleton(agent, filename, task) {
  return [
    `# ${filename}`,
    "",
    `Generated by the mock executor for agent "${agent.id}" (stage "${task.stageId ?? "unknown"}").`,
    "",
    `This is a placeholder artifact — no real content-generating executor is configured. ` +
      `Set runtime.defaultExecutor in runtime.config.json (or pass --executor) to a real ` +
      `implementation once one is available.`,
    "",
    `STATUS: ${task.gateStatus ?? "DRAFT"}`,
  ].join("\n");
}

// Rewrites the last STATUS line in a document (skipping trailing blank and
// blockquote lines), mirroring adf-core's own extractStatusLine
// convention — see adf-core/lib/fs-utils.mjs.
function withStatus(content, status) {
  if (!status) return content;
  const lines = content.split(/\r?\n/);
  for (let i = lines.length - 1; i >= 0; i--) {
    const trimmed = lines[i].trim();
    if (trimmed === "" || trimmed.startsWith(">")) continue;
    if (/^\*{0,2}STATUS:\s*[A-Z0-9_]+\*{0,2}$/.test(trimmed)) {
      lines[i] = lines[i].replace(/STATUS:\s*[A-Z0-9_]+/, `STATUS: ${status}`);
    }
    break;
  }
  return lines.join("\n");
}

export class MockExecutor extends AgentExecutor {
  constructor({ simulateSteps = 0, stepDelayMs = 10 } = {}) {
    super();
    this.simulateSteps = simulateSteps;
    this.stepDelayMs = stepDelayMs;
  }

  get name() {
    return "mock";
  }

  async *run({ agent, task, contextBundle }) {
    yield { type: "progress", message: `mock executor starting for ${agent.id}` };

    for (let i = 0; i < this.simulateSteps; i++) {
      await new Promise((resolve) => setTimeout(resolve, this.stepDelayMs));
      yield { type: "progress", message: `mock executor step ${i + 1}/${this.simulateSteps}` };
    }

    const produces = task.produces ?? agent.outputs ?? [];
    const artifacts = [];
    for (const filename of produces) {
      if (!filename.endsWith(".md")) {
        artifacts.push({ filename, type: "non-document", content: null, note: "mock executor does not generate code" });
        continue;
      }
      const template = resolveTemplate(filename);
      const base = template ?? genericSkeleton(agent, filename, task);
      const content = withStatus(base, task.gateStatus);
      artifacts.push({ filename, type: filename.replace(/\.md$/, ""), content });
    }

    yield {
      type: "result",
      content: `Mock execution of ${agent.id} for stage "${task.stageId ?? "unknown"}" complete.`,
      artifacts,
      dependencyIds: contextBundle?.dependencyIds ?? [],
    };
  }
}
